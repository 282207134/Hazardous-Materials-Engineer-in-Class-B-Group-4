function sortPhrases(a: [string, string], b: [string, string]): number {
  return b[0].length - a[0].length;
}

export function hasJapaneseKana(text: string): boolean {
  return /[ぁ-ゖァ-ヺー]/.test(text);
}

/** 中国語主体の行を日本語学習向けに変換（完全な翻訳ではない） */
export function translateZhLineToJa(text: string): string {
  let result = text;

  for (const [zh, ja] of [...JA_PHRASES].sort(sortPhrases)) {
    result = result.split(zh).join(ja);
  }

  for (const [pattern, replacement] of JA_REPLACEMENTS) {
    result = result.replace(pattern, replacement);
  }

  for (const [pattern, replacement] of JA_RULES) {
    result = result.replace(pattern, replacement);
  }

  return result
    .replace(/，/g, "、")
    .replace(/；/g, "。")
    .replace(/。+/g, "。")
    .replace(/\(固体或液体\)/g, "（固体または液体）")
    .replace(/\(40℃以下\)/g, "（40℃以下）")
    .replace(/\s+/g, " ")
    .trim();
}
